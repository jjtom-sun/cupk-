-- 创建视图
CREATE VIEW V_作业项目材料费 AS
SELECT 
    A.单据号, A.预算单位, A.井号, A.预算金额, A.预算人, A.预算日期,
    A.开工日期, A.完工日期, A.施工单位, A.施工内容,
    A.材料费, A.人工费, A.设备费, A.其它费用, A.结算金额,
    A.结算人, A.结算日期, A.入账金额, A.入账人, A.入账日期,
    B.物码, B.消耗数量, B.单价,
    C.名称规格 AS 材料名称, C.计量单位
FROM 作业项目表 A
LEFT JOIN 材料费表 B ON A.单据号 = B.单据号
LEFT JOIN 物码表 C ON B.物码 = C.物码;
GO
