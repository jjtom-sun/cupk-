IF EXISTS(SELECT * FROM sys.databases WHERE name='zyxt')
    DROP DATABASE zyxt;
CREATE DATABASE zyxt;
GO

USE zyxt;
GO

-- 创建基础表
CREATE TABLE 单位代码表(
    单位代码 CHAR(10) PRIMARY KEY,
    单位名称 NVARCHAR(50) NOT NULL
);
GO

CREATE TABLE 油水井表(
    井号 CHAR(10) PRIMARY KEY,
    井别 CHAR(4) NOT NULL,
    单位代码 CHAR(10) NOT NULL
);
GO

CREATE TABLE 施工单位表(
    施工单位名称 NVARCHAR(50) PRIMARY KEY
);
GO

CREATE TABLE 物码表(
    物码 CHAR(10) PRIMARY KEY,
    名称规格 NVARCHAR(50) NOT NULL,
    计量单位 CHAR(10) NOT NULL
);
GO

CREATE TABLE 作业项目表(
    单据号 CHAR(20) PRIMARY KEY,
    预算单位 CHAR(10) NOT NULL,
    井号 CHAR(10) NOT NULL,
    预算金额 MONEY NOT NULL,
    预算人 NVARCHAR(20) NOT NULL,
    预算日期 DATE NOT NULL,
    开工日期 DATE NULL,
    完工日期 DATE NULL,
    施工单位 NVARCHAR(50) NULL,
    施工内容 NVARCHAR(100) NULL,
    材料费 MONEY NULL,
    人工费 MONEY NULL,
    设备费 MONEY NULL,
    其它费用 MONEY NULL,
    结算金额 MONEY NULL,
    结算人 NVARCHAR(20) NULL,
    结算日期 DATE NULL,
    入账金额 MONEY NULL,
    入账人 NVARCHAR(20) NULL,
    入账日期 DATE NULL
);
GO

CREATE TABLE 材料费表(
    单据号 CHAR(20) NOT NULL,
    物码 CHAR(10) NOT NULL,
    消耗数量 INT NOT NULL,
    单价 MONEY NOT NULL,
    PRIMARY KEY(单据号,物码)
);
GO

